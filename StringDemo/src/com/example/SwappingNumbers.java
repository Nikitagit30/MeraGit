package com.example;

public class SwappingNumbers {

	public static void main(String[] args) {

int a=10,b=20,temp=0;
System.out.println("Before swapping numbers are:"+a);
System.out.println("Before swapping numbers are:"+b);

temp=a;
a=b;
b=temp;




System.out.println("Before swapping numbers are:"+a);
System.out.println("Before swapping numbers are:"+b);



	}

}
